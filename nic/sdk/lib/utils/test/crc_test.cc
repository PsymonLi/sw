#include "lib/utils/crc_fast.hpp"
#include "lib/utils/crc_fast.cc"
#include "string.h"

int main()
{
    unsigned char buf[64] = {
        0x00,0x00,0x00,0x00,0x11,0x00,0x00,0x00,
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
        0x00,0x02,0x00,0x00,0x21,0x00,0x00,0x00,
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
        0x00,0x00,0x00,0x02,0x00,0x00,0x01,0x04,
        0x00,0x00,0x64,0x10,0x00,0x00,0x00,0x00,
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
        0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00
    };

    unsigned int crc = 0;
    sdk::utils::crcFast *crcgen;

    crcgen = sdk::utils::crcFast::factory(0);
    for (int i = 0; i < 1024*1024; i++) {
        crc = crcgen->compute_crc(buf, sizeof(buf), 0);
    }
    printf("%#x\n", crc);
    return 0;
}

