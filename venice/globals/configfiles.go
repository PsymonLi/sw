package globals

const (

	// RegistryConfigFile is used to store the docker registry
	RegistryConfigFile = "/etc/pensando/shared/common/registry.conf"

	// FilebeatConfigFile is used by Filebeat and it contains config params
	FilebeatConfigFile = "/etc/pensando/" + Filebeat + "/filebeat.yml"

	// FilebeatFieldsFile is used by Filebeat and it contains logging fields
	// mapping for elasticsearch indexing.
	FilebeatFieldsFile = "/etc/pensando/" + Filebeat + "/logging_fields.yml"

	// ElasticDiscoveryConfigFile is used by Elastic for Node discovery
	ElasticDiscoveryConfigFile = "/etc/pensando/" + ElasticSearch + "/elastic-discovery/unicast_hosts.txt"

	// ElasticMgmtConfigFile is used by Elastic for binding to MgmtAddr and publishing to peers
	ElasticMgmtConfigFile = "/etc/pensando/" + ElasticSearch + "/mgmt_env.sh"

	// ElasticDataVolumeDir is used by Elastic for storing indexed data
	ElasticDataVolumeDir = "/var/lib/pensando/" + ElasticSearch + "/elasticsearch/data"

	// EtcdConfigFile is written by CMD when starting cluster
	EtcdConfigFile = "/etc/pensando/" + Etcd + "/etcd.conf"

	// APIServerConfigFile is used by ApiServer
	APIServerConfigFile = "/etc/pensando/" + APIServer + "/apiserver.conf"

	// NtpConfigFile is generated by NTP service for chronyd format
	NtpConfigFile = "/etc/pensando/" + Ntp + "/chrony.conf"

	// InfluxConfigFile is used by Influx service
	InfluxConfigFile = "/etc/pensando/" + Influx + "/influxdb.conf"
)
